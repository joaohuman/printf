#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t		i;
	size_t		len;

	len = ft_strlen(s);
	i = len;
	if (c == '\0')
		return ((char *)s + len);
	while (i > 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	if (s[i] == (char)c)
		return ((char *)s + i);
	return (NULL);
}
